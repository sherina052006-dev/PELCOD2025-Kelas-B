tugas = float(input ("Masukkan Nilai Tugas: "))
uts = float(input ("Masukkan Nilai uts: "))
uas = float(input ("Masukkan Nilai uas: ")) 

nilai_tugas = 0.30
nilai_uts = 0.30
nilai_uas =0.40

nilai_akhir = (tugas * nilai_tugas) + (uts * nilai_uts) + (uas * nilai_uas )

print("Nilai Akhir Andi:", nilai_akhir)